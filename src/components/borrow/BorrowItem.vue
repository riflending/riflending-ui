<template>
  <div class="borrow-item dialog">
    <v-list-item>
      <v-row class="my-5 mx-0 d-flex align-center">
        <v-col cols="3">
          <v-row class="d-flex align-center">
            <v-col cols="6" class="pa-0 d-flex justify-end">
              <v-list-item-avatar tile size="40">
                <v-img :src="require(`@/assets/tokens/${token.logo}.png`)" />
              </v-list-item-avatar>
            </v-col>
            <v-col cols="6" class="pa-0 d-flex justify-start">
              <v-list-item-subtitle class="item d-flex justify-start">
                {{ token.symbol }}
              </v-list-item-subtitle>
            </v-col>
          </v-row>
        </v-col>
        <v-col cols="3">
          <v-list-item-subtitle class="item">
            {{ price | formatPrice }}<span class="ml-2 itemInfo">usd</span>
          </v-list-item-subtitle>
        </v-col>
        <v-col cols="2">
          <v-list-item-subtitle class="item"> {{ apr }}% </v-list-item-subtitle>
        </v-col>
        <v-col cols="4" class="px-0">
          <v-row class="ma-0">
            <v-col cols="9" class="pa-0 d-flex align-center">
              <v-list-item-subtitle class="item">
                {{ borrowBalance | formatToken(token.decimals) }}
              </v-list-item-subtitle>
            </v-col>
            <v-col cols="3" class="pa-0">
              <v-btn class="pa-0" icon @click="dialog = !dialog">
                <svg
                  width="11"
                  height="32"
                  viewBox="0 0 11 32"
                  fill="none"
                  xmlns="http://www.w3.org/2000/svg"
                >
                  <path
                    d="M1 1L9 16L1 31"
                    stroke="#008CFF"
                    stroke-width="2"
                    stroke-linecap="round"
                  />
                </svg>
              </v-btn>
            </v-col>
          </v-row>
        </v-col>
      </v-row>
    </v-list-item>
    <v-divider />
    <template v-if="dialog">
      <BorrowDialog :data="dataObject" @closed="reset" />
    </template>
  </div>
</template>

<script>
import { mapState } from 'vuex'
import BorrowDialog from '@/components/dialog/borrow/BorrowDialog.vue'

export default {
  name: 'BorrowItem',
  components: {
    BorrowDialog,
  },
  props: {
    market: {
      type: Object,
      required: true,
    },
  },
  data() {
    return {
      token: {
        name: null,
        symbol: null,
        decimals: 0,
        logo: null,
      },
      price: 0,
      borrowRate: 0,
      cash: 0,
      dialog: false,
      borrowBalance: 0,
    }
  },
  computed: {
    ...mapState({
      account: (state) => state.Session.account,
    }),
    apr() {
      return this.borrowRate.toFixed(2)
    },
    dataObject() {
      return {
        flag: this.dialog,
        borrowRate: this.borrowRate,
        price: this.price,
        token: this.token,
        market: this.market,
      }
    },
  },
  mounted() {
    this.$parent.$parent.$parent.$on('reload', this.reset)
  },
  created() {
    // set data token
    this.token = this.market.token
    this.market
      .borrowBalanceCurrent(this.account)
      .then((balance) => {
        this.borrowBalance = Number(balance)
        return this.market.getPriceInDecimals()
      })
      // set price
      .then((price) => {
        this.price = price
        return this.market.getBorrowRate()
      })

      // set borrow rate block
      .then((borrowRatePerBlock) => {
        this.borrowRate = borrowRatePerBlock
        return this.market.getCash()
      })
      .then((cash) => {
        this.cash = cash
      })
  },
  methods: {
    reset() {
      this.dialog = false
      this.market
        .borrowBalanceCurrent(this.account)
        .then((balance) => {
          this.borrowBalance = balance
          return this.market.getPriceInDecimals()
        })
        // set price
        .then((price) => {
          this.price = price
          return this.market.getBorrowRate()
        })
        // set borrow rate block
        .then((borrowRatePerBlock) => {
          this.borrowRate = borrowRatePerBlock
          return this.market.getCash()
        })
        // set cash
        .then((cash) => {
          this.cash = cash
        })
      this.$emit('dialogClosed')
    },
  },
}
</script>
